            <h1 class="mt-3 text-3xl font-extrabold tracking-tight text-slate-950 sm:text-5xl">How Small Clinics Can Keep Up With Visit Notes Without Staying Late</h1>
            <p class="mt-6 text-lg leading-8 text-slate-600">
                Small clinics usually fall behind on visit notes for understandable reasons: back-to-back appointments, limited staff, and too many tools that do not work well together. A better approach is not writing longer notes. It is using a consistent structure that is fast to complete and easy to follow later.
            </p>

            <div class="mt-10 space-y-9">
                <section>
                    <h2 class="text-2xl font-bold text-slate-950">1. Capture the reason for visit first</h2>
                    <p class="mt-3 leading-7 text-slate-600">
                        Start with the patient or client goal in plain language. This anchors the note and keeps the rest of the documentation focused on the reason they came in today.
                    </p>
                </section>

                <section>
                    <h2 class="text-2xl font-bold text-slate-950">2. Write natural notes before adding structure</h2>
                    <p class="mt-3 leading-7 text-slate-600">
                        Get the core clinical story down first: what you observed, what mattered, and what changed. You can add checkboxes or structured fields after the core note is captured. This keeps note quality from dropping when the day gets busy.
                    </p>
                </section>

                <section>
                    <h2 class="text-2xl font-bold text-slate-950">3. Record care provided and immediate response</h2>
                    <p class="mt-3 leading-7 text-slate-600">
                        Add what was provided during the visit and how the patient or client responded. This helps continuity when they return and makes follow-up visits more efficient.
                    </p>
                </section>

                <section>
                    <h2 class="text-2xl font-bold text-slate-950">4. Add the plan and follow-up clearly</h2>
                    <p class="mt-3 leading-7 text-slate-600">
                        End each note with the next step: return timing, home guidance, or monitoring plan. A clear plan prevents care decisions from getting lost between visits.
                    </p>
                </section>

                <section>
                    <h2 class="text-2xl font-bold text-slate-950">5. Use reminder and follow-up tracking</h2>
                    <p class="mt-3 leading-7 text-slate-600">
                        Notes are only part of the workflow. If there is no follow-up tracking, important care can disappear after checkout. Keep reminders and return prompts visible so the team can act on them.
                    </p>
                </section>
            </div>

            <section class="mt-12 rounded-xl border border-slate-200 bg-[#f8faf7] p-6">
